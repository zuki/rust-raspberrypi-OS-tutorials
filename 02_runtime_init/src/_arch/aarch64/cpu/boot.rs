//! アーキテクチャ固有のブートコード。
//!
//! # オリエンテーション
//!
//! archモジュールはpath属性を使って汎用モジュールにインポートされるので
//! このファイルのパスは次の通り:
//!
//! crate::cpu::boot::arch_boot

use crate::runtime_init;

// このファイルに対応するアセンブリファイル。
global_asm!(include_str!("boot.s"));

//--------------------------------------------------------------------------------------------------
// パブリックコード

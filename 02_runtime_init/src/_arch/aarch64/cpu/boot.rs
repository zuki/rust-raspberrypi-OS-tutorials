/// `kernel`バイナリのRust側エントリ。
///
/// この関数はアセンブリファイルの`_start`関数から呼び出される。
///
/// # 安全性
///
/// - `bss`セクションはまだ初期化されていない。コードはbssをいかなる方法であれ、使用または参照してはならない。
#[no_mangle]
pub unsafe fn _start_rust() -> ! {
    runtime_init::runtime_init()
}
